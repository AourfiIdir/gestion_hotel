
CREATE TABLE IF NOT EXISTS hotels (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nom VARCHAR(120) NOT NULL,
    address VARCHAR(255) NOT NULL,
    notation INT NOT NULL CHECK (notation >= 0 AND notation <= 5)
);

CREATE TABLE IF NOT EXISTS chambres (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hotel_id BIGINT NOT NULL,
    etage INT NOT NULL,
    type VARCHAR(50) NOT NULL,
    prix DECIMAL(10, 2) NOT NULL CHECK (prix >= 0),
    num INT NOT NULL,
    CONSTRAINT fk_chambre_hotel
        FOREIGN KEY (hotel_id) REFERENCES hotels(id)
        ON DELETE CASCADE,
    CONSTRAINT uq_chambre_hotel_num UNIQUE (hotel_id, num)
);

CREATE TABLE IF NOT EXISTS clients (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hotel_id BIGINT NOT NULL,
    nom VARCHAR(120) NOT NULL,
    prenom VARCHAR(120) NOT NULL,
    CONSTRAINT fk_client_hotel
        FOREIGN KEY (hotel_id) REFERENCES hotels(id)
        ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS produits (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    hotel_id BIGINT NOT NULL,
    nom VARCHAR(120) NOT NULL,
    prix DECIMAL(10, 2) NOT NULL CHECK (prix >= 0),
    CONSTRAINT fk_produit_hotel
        FOREIGN KEY (hotel_id) REFERENCES hotels(id)
        ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS reservations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    debut DATE NOT NULL,
    fin DATE NOT NULL,
    client_id BIGINT NOT NULL,
    chambre_id BIGINT NOT NULL,
    reserve_status VARCHAR(40),
    CONSTRAINT ck_reservation_dates CHECK (fin >= debut),
    CONSTRAINT fk_reservation_client
        FOREIGN KEY (client_id) REFERENCES clients(id)
        ON DELETE CASCADE,
    CONSTRAINT fk_reservation_chambre
        FOREIGN KEY (chambre_id) REFERENCES chambres(id)
        ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS sejours (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reservation_id BIGINT NOT NULL,
    facturation DECIMAL(12, 2),
    CONSTRAINT fk_sejour_reservation
        FOREIGN KEY (reservation_id) REFERENCES reservations(id)
        ON DELETE CASCADE,
    CONSTRAINT uq_sejour_reservation UNIQUE (reservation_id)
);

CREATE TABLE IF NOT EXISTS consomations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sejour_id BIGINT NOT NULL,
    quantite INT NOT NULL CHECK (quantite >= 0),
    CONSTRAINT fk_consomation_sejour
        FOREIGN KEY (sejour_id) REFERENCES sejours(id)
        ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS consomation_produits (
    consomation_id BIGINT NOT NULL,
    produit_id BIGINT NOT NULL,
    PRIMARY KEY (consomation_id, produit_id),
    CONSTRAINT fk_cp_consomation
        FOREIGN KEY (consomation_id) REFERENCES consomations(id)
        ON DELETE CASCADE,
    CONSTRAINT fk_cp_produit
        FOREIGN KEY (produit_id) REFERENCES produits(id)
        ON DELETE CASCADE
);
